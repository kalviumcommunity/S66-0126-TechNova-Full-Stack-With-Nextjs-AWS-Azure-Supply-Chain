'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { cn } from '@/lib/utils'

import { UserRole } from '@prisma/client'

export function Sidebar() {
  const pathname = usePathname()

  // This will be replaced with actual auth context
  const user = {
    name: 'User',
    email: 'user@example.com',
    role: 'USER' as UserRole,
  }

  const userNavItems = [
    { label: 'Dashboard', href: '/dashboard', icon: HomeIcon },
    { label: 'My Bookings', href: '/bookings', icon: BookingIcon },
    { label: 'Find Parking', href: '/parking-lots', icon: SearchIcon },
    { label: 'Settings', href: '/settings', icon: SettingsIcon },
  ]

  const adminNavItems = [
    { label: 'Admin Dashboard', href: '/admin', icon: AdminIcon },
    { label: 'Manage Lots', href: '/admin/parking-lots', icon: ParkingIcon },
    { label: 'Reports', href: '/admin/reports', icon: ReportIcon },
  ]

  const navItems =
    user.role === 'ADMIN' ? [...userNavItems, ...adminNavItems] : userNavItems

  return (
    <aside className="w-64 bg-white border-r border-gray-200 min-h-screen">
      {/* User Info */}
      <div className="p-4 border-b border-gray-200">
        <div className="flex items-center gap-3">
          <div className="w-10 h-10 bg-blue-600 rounded-full flex items-center justify-center">
            <span className="text-white font-semibold text-sm">
              {user.name.charAt(0).toUpperCase()}
            </span>
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-gray-900 truncate">
              {user.name}
            </p>
            <p className="text-xs text-gray-500 truncate">{user.email}</p>
          </div>
        </div>
      </div>

      {/* Navigation */}
      <nav className="p-4">
        <ul className="space-y-1">
          {navItems.map((item) => {
            const Icon = item.icon
            const isActive = pathname === item.href

            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  className={cn(
                    'flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors',
                    isActive
                      ? 'bg-blue-50 text-blue-600'
                      : 'text-gray-700 hover:bg-gray-50'
                  )}
                >
                  <Icon className="w-5 h-5" />
                  <span>{item.label}</span>
                </Link>
              </li>
            )
          })}
        </ul>
      </nav>
    </aside>
  )
}

